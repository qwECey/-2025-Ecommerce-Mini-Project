    // Wait for the DOM to load
    document.addEventListener("DOMContentLoaded", function () {
        const newsletterPopup = document.getElementById("newsletter-popup");
        const feedbackPopup = document.getElementById("feedback-popup");
        const closeBtns = document.querySelectorAll(".close-btn");
        const newsletterForm = document.getElementById("newsletter-form");

        // Function to show the popup
        function showPopup(popup) {
            popup.style.display = "flex";
        }

        // Function to hide the popup
        function hidePopup(popup) {
            popup.style.display = "none";
        }

        // Show the newsletter popup after 3 minutes (180,000 milliseconds)
        setTimeout(() => showPopup(newsletterPopup), 60000);

        // Show the newsletter popup every 7 minutes (420,000 milliseconds) after the first popup
        setInterval(() => showPopup(newsletterPopup), 180000);

        // Close the popup when the close button is clicked
        closeBtns.forEach(btn => {
            btn.addEventListener("click", () => {
                if (newsletterPopup.style.display === "flex") {
                    hidePopup(newsletterPopup);
                } else if (feedbackPopup.style.display === "flex") {
                    hidePopup(feedbackPopup);
                }
            });
        });

        // Close the popup when clicking outside the popup content
        window.addEventListener("click", (event) => {
            if (event.target === newsletterPopup) {
                hidePopup(newsletterPopup);
            } else if (event.target === feedbackPopup) {
                hidePopup(feedbackPopup);
            }
        });

        // Handle form submission
        newsletterForm.addEventListener("submit", (event) => {
            event.preventDefault(); // Prevent the form from submitting
            const email = newsletterForm.querySelector("input").value;

            // Simulate a successful subscription
            hidePopup(newsletterPopup);
            document.getElementById("feedback-message").textContent = `Thank you for subscribing! We'll send updates to ${email}.`;
            showPopup(feedbackPopup);

            // Clear the form
            newsletterForm.reset();
        });
    });
